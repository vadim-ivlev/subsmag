<?php

namespace Rg\ApiBundle\Entity;

/**
 * Tariff
 */
class Tariff
{
    /**
     * @var integer
     */
    private $id;

    /**
     * @var float
     */
    private $price;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set price
     *
     * @param float $price
     *
     * @return Tariff
     */
    public function setPrice($price)
    {
        $this->price = $price;

        return $this;
    }

    /**
     * Get price
     *
     * @return float
     */
    public function getPrice()
    {
        return $this->price;
    }
}
